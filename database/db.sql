CREATE DATABASE IF NOT EXISTS recetas_db;

USE `recetas_db`;

-- Crear la tabla de recetas
CREATE TABLE IF NOT EXISTS `recetas` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY,
    `Nombre` VARCHAR(255) NOT NULL
);

-- Crear la tabla de ingredientes
CREATE TABLE IF NOT EXISTS `ingredientes` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY,
    `Nombre` VARCHAR(255) NOT NULL,
   `Vegetariano` BOOLEAN,
    `Vegano` BOOLEAN
);

-- Crear la tabla de alergenos
CREATE TABLE IF NOT EXISTS `alergenos` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY,
    `Nombre` VARCHAR(255) NOT NULL
);

-- Crear la tabla de recetas_ingredientes
CREATE TABLE IF NOT EXISTS `recetas_ingredientes` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY,
    `ID_RECETA` INT,
    `ID_INGREDIENTE` INT,
    `Cantidad` DECIMAL(10,2), -- Se almacena en gramos
    `Grasa` DECIMAL(10,2),
    `Grasas_saturadas` DECIMAL(10,2),
    `Grasas_trans` DECIMAL(10,2),
    `Colesterol` DECIMAL(10,2),
    `Sodio` DECIMAL(10,2),
    `Carbohidratos` DECIMAL(10,2),
    `Proteina` DECIMAL(10,2),
    `Calorias` DECIMAL(10,2),
    FOREIGN KEY (`ID_RECETA`) REFERENCES `recetas`(`ID`),
    FOREIGN KEY (`ID_INGREDIENTE`) REFERENCES `ingredientes`(`ID`)
);

CREATE TABLE IF NOT EXISTS `restricciones` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY,
    `ID_INGREDIENTE` INT,
    `ID_ALERGENO` INT, # un ingrediente puede tener muchos alergenos
    FOREIGN KEY (`ID_INGREDIENTE`) REFERENCES `ingredientes`(`ID`),
    FOREIGN KEY (`ID_ALERGENO`) REFERENCES `alergenos`(`ID`)
);


